package Lec2;

public class relationalOperators {

  public static void main(String[] args) {
    int a = 3, b = 4;

    System.out.println("a == b is " + (a == b));
    System.out.println("a != b is " + (a != b));
    System.out.println("a < b is " + (a < b));
    System.out.println("a > b is " + (a > b));
    System.out.println("a <= b is " + (a <= b));
    System.out.println("a >= b is " + (a >= b));
  }
}
